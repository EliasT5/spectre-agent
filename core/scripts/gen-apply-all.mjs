#!/usr/bin/env node
/**
 * Generate supabase/_apply_all.sql by concatenating the component SQL in
 * DEPENDENCY order, with idempotency transforms so a fresh paste AND a re-run
 * both succeed. Component files keep their own guards (drop-if-exists for
 * constraints, on-conflict, create-or-replace); this adds the rest.
 *
 *   node scripts/gen-apply-all.mjs
 *
 * Verify against a fresh pgvector DB (Supabase-like): create the `auth` schema +
 * the `supabase_realtime` publication, then apply twice — both must exit 0.
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { resolve } from "node:path";

const dir = resolve(import.meta.dirname, "..", "supabase");

// Dependency order. EXCLUDES _apply_all.sql, seed-*.sql, temp-private.sql.
// The three schedule files each (re)define scheduled_jobs_target_type_check with a
// CUMULATIVE enum (dream ⊂ proactive ⊂ skillopt ⊂ skill_curation), so they must apply in that order
// — the widest (skill-usage) LAST — or a re-add would reject already-inserted rows.
const ORDER = [
  "schema.sql",
  "durable-chat.sql",
  "channels.sql", // ALTERs messages (delivered_at) + channel_accounts -> after durable-chat
  "memory-embeddings.sql",
  "messages-embeddings.sql",
  "ingest.sql",
  "modules.sql",
  "model_backends.sql",
  "module-data.sql",
  "monitor_events.sql",
  "notes.sql",
  "pdf_library.sql",
  "generated-media.sql", // references threads -> after schema.sql
  "push_subscriptions.sql",
  "connected_accounts.sql", // multi-account mail/calendar (Microsoft + Google)
  "scheduled_jobs.sql",
  "scheduled_jobs_routines.sql",
  "skillopt.sql",
  "skillopt-deploy.sql",
  "tempus-native.sql",
  "tool_policies.sql",
  "workshop-realtime.sql",
  "dream-default-schedule.sql", // target_type IN (… dream)
  "proactive-default-schedule.sql", // … + proactive
  "skillopt-schedule.sql", // … + skillopt
  "skill-usage.sql", // … + skill_curation  (widest — LAST)
];

function idempotent(sql) {
  return (
    sql
      // bare CREATE TABLE / INDEX / ADD COLUMN -> IF NOT EXISTS
      .replace(/\bcreate\s+table\s+(?!if\s+not\s+exists)/gi, "CREATE TABLE IF NOT EXISTS ")
      .replace(
        /\bcreate\s+(unique\s+)?index\s+(?!if\s+not\s+exists)/gi,
        (_m, u) => `CREATE ${u ? "UNIQUE " : ""}INDEX IF NOT EXISTS `,
      )
      .replace(/\badd\s+column\s+(?!if\s+not\s+exists)/gi, "ADD COLUMN IF NOT EXISTS ")
      // (realtime ALTER PUBLICATION is already guarded in-source with a
      // pg_publication_tables existence check — leave it; the Supabase target
      // provides the supabase_realtime publication.)
      // CREATE POLICY "name" on <table> -> drop-first (policies have no IF NOT EXISTS)
      .replace(
        /\bcreate\s+policy\s+("[^"]+")\s+on\s+([^\s;]+)/gi,
        (_m, name, table) => `DROP POLICY IF EXISTS ${name} ON ${table};\nCREATE POLICY ${name} ON ${table}`,
      )
      // CREATE TRIGGER name … on <table> -> drop-first (no IF NOT EXISTS for triggers)
      .replace(
        /\bcreate\s+trigger\s+(\w+)([\s\S]*?\son\s+(\w+))/gi,
        (m, name, _rest, table) => `DROP TRIGGER IF EXISTS ${name} ON ${table};\nCREATE TRIGGER ${name}${_rest}`,
      )
  );
}

const header = `-- GENERATED by scripts/gen-apply-all.mjs — DO NOT EDIT BY HAND.
-- Edit the component supabase/*.sql and re-run the generator.
-- Paste into a fresh Postgres (with the pgvector extension) for the full Spectre
-- schema; safe to re-run. Embeddings are vector(768) for the default
-- nomic-embed-text embedder; if EMBED_PROVIDER=openai, set EMBED_DIM=1536 and resize.

-- Quiet the idempotency scaffolding: DROP ... IF EXISTS on a fresh database
-- emits a "does not exist, skipping" NOTICE per object, which reads like the
-- apply skipped things. Session-local; plain WARNING+ still shows.
SET client_min_messages = warning;
`;

// Drift guard: every component supabase/*.sql (minus the known excludes) MUST be
// in ORDER, or it silently never reaches a fresh self-host install — exactly how
// generated_media and connected_accounts went missing. Fail loudly if one drifts.
const EXCLUDED = (f) => f === "_apply_all.sql" || f.startsWith("seed") || f.startsWith("temp");
const onDisk = readdirSync(dir).filter((f) => f.endsWith(".sql") && !EXCLUDED(f));
const missing = onDisk.filter((f) => !ORDER.includes(f));
if (missing.length) {
  console.error(
    `\n✗ SCHEMA DRIFT — these supabase/*.sql are NOT in ORDER and won't ship to fresh installs:\n  ` +
      missing.join("\n  ") +
      `\n  Add them (in dependency order) to ORDER in scripts/gen-apply-all.mjs, then re-run.\n`,
  );
  process.exit(1);
}

let out = header;
for (const f of ORDER) {
  const sql = readFileSync(resolve(dir, f), "utf8");
  out += `\n-- ============================================================\n-- ${f}\n-- ============================================================\n`;
  out += idempotent(sql).trimEnd() + "\n";
}
writeFileSync(resolve(dir, "_apply_all.sql"), out);
console.log(`generated supabase/_apply_all.sql from ${ORDER.length} component files (${out.length} bytes)`);

// Anti-drift: the shell (at the repo root) ships ONLY _apply_all.sql (the
// installer applies it for a local DB) and has no component SQL of its own, so
// it can silently fall behind this — exactly how the generated_media table went
// missing from a self-host install. Keep the root shell copy byte-identical
// automatically. Skips silently when run outside the monorepo layout.
const shellSupabaseDir = resolve(dir, "..", "..", "supabase");
if (existsSync(shellSupabaseDir)) {
  writeFileSync(resolve(shellSupabaseDir, "_apply_all.sql"), out);
  console.log(`  ↳ synced ../supabase/_apply_all.sql (kept in lockstep)`);
}
